package tw.edu.sinica.iis.ants.DB;

public class T_UserTrip {
	private int id;
	private Integer userid;
	private Integer trip_count;
	
	public T_UserTrip(){
	
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getUserid() {
		return userid;
	}

	public void setUserid(int userid) {
		this.userid = userid;
	}

	public int getTrip_count() {
		return trip_count;
	}

	public void setTrip_count(int tripCount) {
		trip_count = tripCount;
	}

	
}
